package com.liuyue.loop;

public class forDemo1 {
    public static void main(String[] args) {
        testFor();
        testFor2();
    }

    public static void testFor() {
        for (int i = 0; i < 3; i++) {
            System.out.println("hello world");

        }
    }
    public static void testFor2() {
        for (int i = 0; i < 100; i++) {
            System.out. println("你好");
        }
    }

}
